export const Category = (category) => {
    if (category === "starter") {
        return (
            "מנות ראשונות"
        )
    }
    if (category === "Soups") {
        return (
            "מרקים"
        )
    }
    if (category === "desserts") {
        return (
            "קינוחים"
        )
    }
    if (category === "MainCourse") {
        return (
            "מנה עיקרית"
        )
    }
}